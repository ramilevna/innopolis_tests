m = [
    [1, -1, 0, -4, 2, 2,-1],
    [1,-1, 0,-4, 2, 2,-1],
    [2,-1, 0,-1, 2,-4, 1],
    [0,-1, 1, 2, 2,-1,-4],
    [1, 2,-4, 2, 0,-1,-1],
    [-4,-1, 2, 1, 2, 0,-1],
    [0,-4, 1,-1, 2,-1, 2],
    [-4, 2, 1, 2, 0,-1,-1],
    [2, 2,-1,-1, 1, 0,-4]
]


def f(pos, a, s, k, n=7):
    s.add(pos)
    if k >= 100:
        return
    else:
        pos = pos + a[pos]
        if (pos < 0) or (pos >= n):
            pos = n - 1
        f(pos, a, s, k+1)


for a in m:
    s = set()
    f(0, a, s, 0)
    print(len(s), s)
